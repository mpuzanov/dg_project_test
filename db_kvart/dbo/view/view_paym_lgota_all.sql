	,ph.occ
	,ph.service_id
	,oh.flat_id
	,oh.bldn_id
	,oh.start_date
	,ph.lgotaAll
	,ph.discount
	,ph.owner_id
	,ph.lgota_id
	,ph.Snorm
	,ph.owner_lgota
	,ph.is_counter
	,ph.subsid_only
FROM dbo.PAYM_LGOTA_ALL AS ph 
LEFT OUTER JOIN dbo.View_OCC_ALL AS oh 
	ON ph.fin_id = oh.fin_id
	AND ph.occ = oh.occ;
