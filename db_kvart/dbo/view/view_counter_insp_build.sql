CREATE   VIEW [dbo].[view_counter_insp_build]
AS
SELECT c.serial_number
	 , c.service_id
	 , c.build_id
	 , c.unit_id
	 , c.is_build
	 , ci.inspector_value
	 , ci.inspector_date
	 , ci.kol_day
	 , ci.actual_value
	 , ci.value_vday
	 , ci.fin_id
	 , c.id AS counter_id
	 , ci.volume_arenda
	 , ci.id
	 , ci.is_info
	 , ci.blocked
	 , ci.volume_odn
	 , ci.norma_odn
	 , ci.volume_direct_contract
FROM dbo.Counter_inspector AS ci 
	RIGHT OUTER JOIN dbo.Counters As c 
		ON ci.counter_id = c.id
WHERE c.is_build = 1;
