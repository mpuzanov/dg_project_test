-- dbo.view_suppliers source

CREATE   VIEW [dbo].[view_suppliers]
AS
SELECT
	sup.id
	,sup.service_id
	,sa.Name
	,sa.adres
	,sa.telefon
	,sa.fio
	,sup.sup_id
	,sup.id_key
	,sa.id_accounts
	,sa.bank_account
	,sa.account_one
	,s.Name AS service_name
FROM dbo.Suppliers as sup
INNER JOIN dbo.View_suppliers_all AS sa
	ON sup.sup_id = sa.id
INNER JOIN dbo.View_services as s
	ON sup.service_id = s.id;
go

