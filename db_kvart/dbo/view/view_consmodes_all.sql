CREATE   VIEW [dbo].[view_consmodes_all]
AS
SELECT
	b.fin_current AS fin_id
	,cl.occ
	,cl.service_id
	,cl.sup_id
	,cl.source_id
	,cl.mode_id
	,cl.koef
	,cl.subsid_only
	,cl.is_counter
	,cl.account_one
	,cl.dog_int
FROM dbo.CONSMODES_LIST AS cl
JOIN dbo.OCCUPATIONS AS o 
	ON cl.occ = o.occ
JOIN dbo.Flats AS f 
	ON o.flat_id = f.Id
JOIN dbo.Buildings AS b 
	ON f.bldn_id=b.id
UNION ALL
SELECT
	ch.fin_id
	,ch.occ
	,ch.service_id
	,ch.sup_id
	,ch.source_id
	,ch.mode_id
	,ch.koef
	,ch.subsid_only
	,ch.is_counter
	,ch.account_one	
	,null AS dog_int
FROM dbo.PAYM_HISTORY ch;
go

