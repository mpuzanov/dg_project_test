CREATE   VIEW [dbo].[view_dimbuildings]
AS
SELECT        dbo.Towns.NAME AS town_name, dbo.Streets.Name AS street_name, dbo.Buildings.nom_dom, dbo.Buildings.id AS build_id, dbo.Buildings.street_id, dbo.Buildings.tip_id, dbo.Buildings.town_id, dbo.Buildings.nom_dom_sort, 
                         dbo.Streets.prefix AS prefix_street
FROM            dbo.Buildings INNER JOIN
                         dbo.Streets ON dbo.Buildings.street_id = dbo.Streets.Id INNER JOIN
                         dbo.Towns ON dbo.Buildings.town_id = dbo.Towns.ID AND dbo.Streets.town_id = dbo.Towns.ID;
go

