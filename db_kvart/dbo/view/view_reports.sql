-- dbo.view_reports source

CREATE   VIEW [dbo].[view_reports]
AS
	SELECT r.Level1
		 , r.Level2
		 , r.name
		 , r.FileName
		 , r.Procedures
		 , r.REPORT_BODY
		 , r.NO_VISIBLE
		 , r.Id
		 , r.ID_PARENT
		 , r.APP
		 , r.SIZE_REPORT_BODY
		 , r.date_edit
		 , r.Personal_access
		 , r.pasport_access
		 , r.FileDateEdit
		 , CAST(CASE WHEN(Report_preview IS NOT NULL) THEN 1 ELSE 0 END AS BIT) AS sample_true
		 , r.is_edit
	FROM dbo.Reports AS r
		CROSS JOIN dbo.Users AS us
	WHERE (us.login = system_user)
		AND (r.Personal_access = us.blocked_personal OR us.blocked_personal = CAST(0 AS BIT));
go

