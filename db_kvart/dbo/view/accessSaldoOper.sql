CREATE   VIEW [dbo].[accessSaldoOper]
AS
SELECT     a.area_id, a.group_id
FROM         dbo.ALLOWED_AREAS AS a INNER JOIN
                      dbo.USERS AS u ON a.user_id = u.id INNER JOIN
                      dbo.GROUP_MEMBERSHIP ON a.user_id = dbo.GROUP_MEMBERSHIP.user_id AND a.group_id = dbo.GROUP_MEMBERSHIP.group_id
WHERE     (a.op_id = 'слдо') AND (u.login = SYSTEM_USER)
go

