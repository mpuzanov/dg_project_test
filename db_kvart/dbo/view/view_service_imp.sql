AS
SELECT        dbo.Service_imp.name, dbo.Service_imp.id, dbo.Services.name AS name_db, dbo.Services.short_name AS short_name_db
FROM            dbo.Service_imp INNER JOIN
                         dbo.Services ON dbo.Service_imp.id = dbo.Services.id;
