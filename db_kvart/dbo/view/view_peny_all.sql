CREATE   VIEW [dbo].[view_peny_all]
AS
	SELECT t.*
		 , gv.StavkaCB
		 , gv.PenyProc
		 , gv.StrMes
	FROM (

		SELECT ps.fin_id
			 , ps.occ
			 , ps.dolg
			 , ps.dolg_peny
			 , ps.paid_pred
			 , ps.peny_old
			 , ps.PaymAccount
			 , ps.PaymAccount_peny
			 , ps.peny_old_new
			 , ps.Penalty_added
			 , ps.kolday
			 , ps.Penalty_value
			 , ps.debt_peny
			 , ps.metod
			 , ps.Data_rascheta
			 , ps.occ1
			 , ps.sup_id
			 , ps.penalty_calc
		FROM dbo.Peny_all AS ps 

	) AS t
		JOIN dbo.Global_values AS gv ON t.fin_id = gv.fin_id;
go

